    private void atualizarTabela() {
        
        AnimalBean an;
        DefaultTableModel modelo = (DefaultTableModel) tabela.getModel();
        modelo.setNumRows(0);
        try {
            List<AnimalBean> listaAnimal = daoAnimal.getLista(this.txNumero.getText().trim(), "cod_animal");

            if (listaAnimal.size() == 0) {
                txMsg.setText("NENHUM ANIMAL FOI ENCONTRADO POR CÃ“DIGO: \"" + this.txNumero.getText().trim() + "\" !");
            } else {
                txMsg.setText("");
            }

            for (int i = 0; i < listaAnimal.size(); i++) {
                an = new AnimalBean();
                an = listaAnimal.get(i);
                modelo.addRow(new Object[]{
                            an.getCodAnimal(),
                            an.getSexo(),
                            an.getCor(),
                            an.getPesoEntrada(),
                            an.getValorCompra()
                        });

            }
        } catch (Exception e) {
            e.printStackTrace();
        }

    }
